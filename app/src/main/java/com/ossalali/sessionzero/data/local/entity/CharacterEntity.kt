package com.ossalali.sessionzero.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey

@Entity(tableName = "characters")
data class CharacterEntity(
    @PrimaryKey val id: String,
    val name: String = "",
    val pronouns: String = "",
    val level: Int = 1,
    val xp: Int = 0,
    val alignment: String = "",
    val className: String? = null,
    val subclass: String? = null,
    val species: String? = null,
    val speciesLineage: String? = null,
    val background: String? = null,
    val abilityScoreBonuses: String = "{}",
    val originFeat: String? = null,
    val baseStr: Int = 10,
    val baseDex: Int = 10,
    val baseCon: Int = 10,
    val baseInt: Int = 10,
    val baseWis: Int = 10,
    val baseCha: Int = 10,
    val abilityScoreMethod: String? = null,
    val skillProficiencies: String = "[]",
    val equipmentChoice: String? = null,
    val coinsCp: Int = 0,
    val coinsSp: Int = 0,
    val coinsGp: Int = 0,
    val coinsPp: Int = 0,
    val personalityTraits: String = "",
    val ideals: String = "",
    val bonds: String = "",
    val flaws: String = "",
    val appearance: String = "{}",
    val backstory: String = "",
    val alliesAndOrganizations: String = "",
    val additionalNotes: String = "",
    val portraitUrl: String = "",
    val feats: String = "[]",
    val languages: String = "[]",
    val knownCantrips: String = "[]",
    val preparedSpells: String = "[]",
    val hpOverride: Int? = null,
    val acOverride: Int? = null,
    val spellSaveDCOverride: Int? = null,
    val createdAt: String = "",
    val updatedAt: String = "",
)
